#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef double db;

const int MAX = 1e5 + 10;
ll n,m,fa[MAX],a,b;
char c;

void init()
{
    for(ll i = 0;i < n;i++) fa[i] = i;
}

ll find(ll x)
{
    return fa[x] == x ? x : fa[x] = find(fa[x]);
}

void merge(ll x,ll y)
{
    ll fx = find(x),fy = find(y);
    if(fx == fy) return;
    fa[fy] = fx;
}

int main()
{
	ios::sync_with_stdio(false);
	cin.tie(0); cout.tie(0);
	ll t = 1;
//	cin >> t;
	while(t--)
	{
		cin >> n >> m;
        init();
        for(ll i = 0;i < m;i++)
        {

            cin >> c >> a >> b;
            if(c == 'M') merge(a,b);
            else 
            {
                ll fx = find(a),fy = find(b);
                if(fx == fy) cout << "Yes" << "\n";
                else cout << "No" << "\n";
            }
        }
	}
	return 0;
}