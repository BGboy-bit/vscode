void add(vector<ll> &v1,vector<ll> &v2)
{
    ll t = 0;
    for(ll i = 0;i < l1 || i < l2;i++)
    {
        if(i < l1) t += v1[i];
        if(i < l2) t += v2[i];
        v3.push_back(t % 10);
        t /= 10;
    }
    if(t > 0) v3.push_back(1);
}

