#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef double db;

const int MAX = 2e5 + 10;
ll n;

int main()
{
	ios::sync_with_stdio(false);
	cin.tie(0); cout.tie(0);
	ll t = 1;
	cin >> t;
	while(t--) {
		cin >> n;
        vector<int> v(n + 1, 0), cnt(n + 1, 0);
        for(ll i = 0; i < n; i ++) {
            cin >> v[i];
            if(v[i] <= n) cnt[v[i]] ++;
        }
        ll res = 0;
        while(cnt[res]) res ++;
        if(res == n) {
            cout << "NO" << "\n";
            continue;
        }
        else if(cnt[res + 1] == 0) {
            cout << "YES" << "\n";
            continue;
        }
        ll l = n + 1, r = 0;
        for(ll i = 0; i < n; i ++) {
            if(v[i] != res + 1) continue;
            l = min(l,i);
            r = max(r,i);
        }
        for(ll i = l; i <= r; i ++) {
            if(v[i] <= n) cnt[v[i]] --;
        }
        ll ris = 0;
        while(cnt[ris]) ris ++;
        if(ris == res) cout << "YES" << "\n";
        else  cout << "NO" << "\n"; 
    }    
	return 0;
}