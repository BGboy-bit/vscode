#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef double db;

const int MAX = 1e5 + 10;

void Solve() {
     ll a, b, ans = INT_MAX;
     cin >> a >> b;
     for(ll i = 1; i <= MAX; i ++) {
        ll n = 0, m = 0;
        if(a % i) m ++;
        if(b % i) n ++;
        ll res = (a / i) + (b / i) + (i - 1) + n + m;
        ans = min(ans,res);
     }
     cout << ans << "\n";
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    ll T = 1;
    cin >> T;
    while(T --) {
        Solve();
    }
    return 0;
}