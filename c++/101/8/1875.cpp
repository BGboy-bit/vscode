using namespace std;
typedef long long ll;
typedef double db;

const int MAX = 1e4 + 10;
ll n,a[MAX];
queue<ll> q;

void Solve() {
    while(cin >> n && n != 0) {
        ll f = 0,res = 0,cnt = 0;
        for(ll i = 0; i < n; i++) {
            cin >> a[i];
            if(a[i] > res) {//有新输入
                    for(ll j = a[i] - 1;j > res;j--) q.push(j);
                    res = a[i];
                }
            else {//输出栈顶元素
                if(a[i] != q.front()) f = 1;
                else q.pop();
            }
        }
        if(f) cout << "NO" << "\n";
        else cout << "YES" << "\n";
    }   
}


