const int MAX = 1e5 + 10;
ll n,a[MAX];

void Solve() {
    cin>>n;
    for(ll i = 1; i <= n; i ++) a[i]=i;
    ll sum=1;
    for(ll i = 2 * n; i >= n; i--) sum *= i;
    for(ll i = n; i >= 1; i --) sum /= i;
    sum /= n + 1;
    sum /= n;
    cout<<sum<<'\n';
    do {
        ll p = 1;
        for(ll i = 1; i + 2 <= n; i ++) {
            for(ll j = i + 1; j + 1 <= n; j ++) {
                for(ll k = j + 1; k <= n; k ++) {
                    if(a[i] > a[k] && a[k] > a[j]) {
                        p = 0; 
                        break;
                    }
                    if(p == 0) break;
                }
                if(p == 0) break;
            }
            if(p == 0) break;
        }
        if(p) {
            sum ++;
            for(ll i = 1; i <= n; i ++) {
            cout << a[i];
            if(i != n) cout << " ";
            else cout << "\n";
            }
        }
    }while (next_permutation(a + 1, a + n + 1)); 
}


