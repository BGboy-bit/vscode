	// cin.tie(0); cout.tie(0);
	ll t = 1;
//	cin >> t;
	while(t--)
	{
        while(getline(cin,s))
        {
            ll cnt = 0;
            for(ll i = 1;i < s.size() - 1;i++)
            {
                if(s[i] == ' ')
                {
                    cnt ++;
                    continue;
                }
                str[cnt] += s[i];
            }
            cout << "\"";
            for(ll i = cnt;i >= 0;i--) 
            {
                cout << str[i];
                str[i].clear();
                if(i != 0) cout << " ";
            }
            cout << "\"\n";
        }
	}
	return 0;
